export const advanceIndex = (index: number, array: string[][]) => (index === array.length - 1 ? 0 : index + 1)

export const calculate = (selector: string) => {
  let index = 0
  let value = 0

  document.querySelectorAll<HTMLInputElement>(selector).forEach(item => {
    if (item.checked) {
      value += 2 ** index
    }

    index++
  })

  return value
}

